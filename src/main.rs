use regex_library::regex_parsing::tokenize::Lexer;
use regex_library::regex_parsing::syntax_tree::{parse_tree, parse_to_nfa};
use regex_library::automata::automata::Parse;



fn main() {
    let test = "(0|(1(01*0)*1))*(0|(1(01*0)*1))*(0|(1(01*0)*1))*(0|(1(01*0)*1))*(0|(1(01*0)*1))*(0|(1(01*0)*1))*(0|(1(01*0)*1))*(0|(1(01*0)*1))*(0|(1(01*0)*1))*(0|(1(01*0)*1))*(0|(1(01*0)*1))*(0|(1(01*0)*1))*(0|(1(01*0)*1))*(0|(1(01*0)*1))*(0|(1(01*0)*1))*(0|(1(01*0)*1))*(0|(1(01*0)*1))*(0|(1(01*0)*1))*(0|(1(01*0)*1))*(0|(1(01*0)*1))*(0|(1(01*0)*1))*(0|(1(01*0)*1))*(0|(1(01*0)*1))*(0|(1(01*0)*1))*(0|(1(01*0)*1))*(0|(1(01*0)*1))*(0|(1(01*0)*1))*(0|(1(01*0)*1))*(0|(1(01*0)*1))*(0|(1(01*0)*1))*(0|(1(01*0)*1))*";

    let mut s = Lexer::new(test);
    let tree = parse_tree(&mut s);
    let nfa = parse_to_nfa(&tree).build();
    let dfa = nfa.to_dfa();
    println!("{:?}", dfa.adj_list);
}